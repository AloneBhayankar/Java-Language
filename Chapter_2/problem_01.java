// 1. What will be the result of the following expression: float a = 7/4 * 9/2
package Chapter_2;
public class problem_01 {
    public static void main(String[] args) {
        float a = 7/4.0f * 9/2.0f;
        System.out.println(a);
    }   
}

// Output: 7.875
// 7 / 4.0f = 1.75f
// 1.75f * 9 = 15.75f
// 15.75f / 2.0f = 7.875f
